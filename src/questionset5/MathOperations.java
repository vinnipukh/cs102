package questionset5;

public class MathOperations {

    public MathOperations(){

    }
    public int add(int int1, int int2){
        return int1+int2;
    }
    public double add(double d1, double d2){
        return d1+d2;
    }
    public int add(int int1, int int2, int int3){
        return int1+int2+int3;
    }

}
